package ch05;


public class Order {
	
	String orderNum;
	String cId;
	String cDate;
	String cName;
	String proNum;
	String addr;
	
	public String getOrderNum() {
		return orderNum;
	}
	
	public String getcId() {
		return cId;
	}
	
	public String getcDate() {
		return cDate;
	}
	
	public String getcName() {
		return cName;
	}
	
	public String getProNum() {
		return proNum;
	}
	
	public String getAddr() {
		return addr;
	}
	
	
}
